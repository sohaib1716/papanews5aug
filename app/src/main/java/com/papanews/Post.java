package com.papanews;

public class Post {

    private int userId;
    private String title;
    private String shortDesc;
    private String longDesc;

    public int getUserId() {
        return userId;
    }

    public String getTitle() {
        return title;
    }

    public String getShortDesc() {
        return shortDesc;
    }

    public String getLongDesc() {
        return longDesc;
    }

}
